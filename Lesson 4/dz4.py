# Пользователь вводит с клавиатуры i, s и n, программа должна
# рассчитать размер ежемесячной выплаты по формуле, а также
# найти, сколько пользователь всего заплатит банку и сколько составит переплата
# Сделать ввод/вывод данных
# m = ежемесячная выплата
# i = годовая процентная ставка
# p – месячная процентная ставка
# s – сумма займа
# n – количество месяцев, на которые взят кредит


i_1 = input("Введите годовую ставку: ")
i = float(i_1)
s_1 = input("Введите сумму займа: ")
s = float(s_1)
n_1 = input("Введите количество месяцев: ")
n = float(n_1)
p = i / (12 * 100)
m = (s * p) * (pow(1 + p, n)) / ((pow(1 + p, n)) - 1)
t_pay = m * n # Общая сумма
o_pay = t_pay - s # Переплата
print(f"Ежемесячный платёж: {m:.2f}")
print(f"Общая сумма выплат: {t_pay:.2f}")
print(f"Переплата: {o_pay:.2f}")


