# Пользователь вводит с клавиатуры информацию о двух планетах: радиус и орбитальную скорость. Программа должна посчитать и
# вывести длину года на первой и второй планетах, а также вывести, правда ли, что на первой планете год длиннее, чем на второй
# Сделать ввод/вывод данных
# L - длина года на планете
# R – радиус орбиты планеты (млн. км.)
# 𝑣 – орбитальная скорость (км/ч)

print("Введите информацию о первой планете: ")
r_1 = float(input("Радиус орбиты (млн. км.): "))
v_1 = float(input("Орбитальная скорость (км/ч): "))

# Длина года на первой планете
k_1 = r_1 * 10**6  # радиус в млн. км.
l_1 = (2 * k_1 * math.pi) / v_1  # длина года в часах
l_1 /= 24*365  # перевод в дни

print("Введите информацию о второй планете:")
r_2 = float(input("Радиус орбиты (млн. км.): "))
v_2 = float(input("Орбитальная скорость (км/ч): "))

# Длина года на второй планете
k_2 = r_2 * 10**6  # радиус в млн. км.
l_2 = (2 * k_2 * math.pi) / v_2  # длина года в часах
l_2 /= 24*365  # перевод в дни

print(f"Длина года на первой планете: {l_1:.1f} дней")
print(f"Длина года на второй планете: {l_2:.1f} дней")
print(f"Год на первой планете длиннее, чем на второй? -> {l_1>l_2}")